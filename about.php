即為電台名稱，為同學服務為電台宗旨，「元智之音」為校內資訊的廣播站，所有校內<br>
的重要活動訊息，只要收聽元智之音就能獲得，不用再費時間去看那一大堆的文字。<br>
<br>
<b><font face="標楷體" size="6">元</font></b>智之音為２４小時全天候播放的學生電台<br>
