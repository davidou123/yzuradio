<b><font color="#0000FF"><a name="元智之音學生廣播電台LOGO設計概念">元智之音學生廣播電台LOGO設計概念</a></font></b><br>
<br>
廣播的歷史久遠，自從愛迪生發明留聲機以來，人類就開始有了機械式的音響，把刻錄在唱片上的訊號經由號角式的喇叭而放大聲音。後來，1906年第佛勒斯特（Lee De 
Forest）完成三極真空管，使人類透過無線電廣播傳遞人類聲音的夢想得以實現。到了現代，電子產品越來越精緻輕薄短小，但其演進形式仍然能看見初始發明時的影子。因此，我們用了ＣＤ這一個從發明之初到現在仍然不變的形狀，來代表著我們廣播電台的精神。即使在資訊發達、電視普及、網路遍佈的現代，仍然持續作廣播，堅信廣播有其魅力及存在之必要性。<br>
而現在是數位化的世代，所有電子影像呈現出來的畫面均是由微小的方塊所組成。連聲音的呈現也用方塊堆疊，高低起伏來代表其音量和頻率。藉由這一原理及融合科技感，使用黑色搭配橘色方塊排列堆疊，組成一個ＣＤ的圖案。來代表著元智之音學生廣播電台。<br>
<br>
<br>
<b><font color="#0000FF"><a name="元智之音學生廣播電台社團辦公室設計概念">元智之音學生廣播電台社團辦公室設計概念</a></font></b><br>
<br>
藉由元智之音ＬＯＧＯ的ＣＤ造型創作理念，將社辦的圖案全由小方塊來堆疊組合而成。而在社團辦公室的左邊牆面上，堆疊出一個大大的ＬＯＧＯ作為社團精神象徵。其位置讓路過的人第一眼就能看見，知道此社辦事屬於元智廣播電台社，並有好的第一印象，而在右邊牆面，我們也沿用了方塊理念，排列出一個方形的佈告欄，來擺放社團的活動花絮。另外在牆面的頂端，我們用方塊排列為成一圈，使社辦產生統整感，讓ＬＯＧＯ與佈告欄的設計能結合在一起，而不會讓人覺得像是分離的兩個圖案。<br>
<br>
<br>
<b><font color="#0000FF"><a name="元智之音學生廣播電台社團吉祥物設計概念">元智之音學生廣播電台社團吉祥物設計概念</a></font></b><br>
<br>
元智之音廣播電台的發源來自於每年聖誕節的短期節目播放，在這個節日的前後，可以聽到廣播電台社的社員發揮一年來的所學，所錄製的特別節目。同時，為配合此盛大的節日，社團對於其節目內容和宣傳上，也會下一番功夫，聖誕節是從西方傳過來的，在西方，聖誕節會下雪，放假的孩子們就會做雪人來玩樂。因此，雪人儼然已成為一個代表性，我們廣電社也會做出雪人來，而且是戴著耳機的雪人。<br>
而在去年，廣電社決定，將一年只有一週的節目播放，改成全年性的廣播節目。而聖誕節的「聖誕點播情」就是廣播的起跑點。於是，我們就拿雪人來做我們社團的吉祥物，當作是一個續舊創新的開始。其雪人身上，配戴了製作廣播節目不可或缺的耳機及麥克風。而後面則背了一個唱片形狀的背包，代表著廣電社的ＬＯＧＯ精神。而此吉祥物，我們將之命名為「瑞迪歐」，取自於「RADIO」這個名字，因為早期的廣播是透過無線電傳送，在西方稱為「radio」，而其發射的電台稱為「無線電台」「radio 
station」。<br>
<br>
<br>
<b><font color="#0000FF"><a name="元智之音學生廣播電台吉祥物「瑞迪歐」的故事">元智之音學生廣播電台吉祥物「瑞迪歐」的故事</a></font></b><br>
<br>
在寒冷的北極地裡，有一個小雪人，某天，有個船隻路過了小雪人所住的地方，還不小心遺留了一個方形盒子。小雪人很好奇，拿了起來好奇的研究，發現這個方形盒子裡有聲音，而且可以聽到好多好多人在唱歌和說故事。<br>
從此之後，他每天起床後會做的第一件事，就是拿出方形盒子，聽裡面的人在說故事，偶爾還會聽聽音樂。而且裡面的人還會用很有趣的方式講很多有趣的事情來聽。小雪人好喜歡裡面的人，他好希望自己也可以跟盒子裡面的人一樣的厲害，可以把話說得那麼的有趣生動。後來有一天小雪人終於知道了這個是收音機，而那些聲音叫做廣播。裡面的人統稱做ＤＪ。於是他決定自己也要當一個ＤＪ，而且是一個很厲害很有趣的ＤＪ。<br>
過沒多久，小雪人在廣大的北極地裡，發現了一個耳機連著麥克風。但是極地裡的其他雪人們都不會用，也沒辦法&#25934;他怎樣變成一個厲害的ＤＪ。於是他決定到大陸上學習。小雪人為自己做了一個圓形、長得像唱片的背包，因為他想到了有一次聽到收音機裡介紹廣播和唱片的歷史故事。<br>
小雪人帶著這兩樣東西，一路上不畏辛苦，跋山涉水的來到了元智廣播電台社，當他看到了元智廣播電台社大大的唱片形狀的圖案，就知道他找到了能夠實現夢想的地方了。元智廣播電台社的人非常的熱心，他們給小雪人取名為「瑞迪歐」，讓他在這裡展開他的ＤＪ修行之路。</p>

